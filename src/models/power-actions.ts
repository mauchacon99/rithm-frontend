/**
 * Represents the actions in the powers of a station.
 */
export interface PowerActions {
  /** Rithm Id. */
  rithmId: string;
  /** Type Action. */
  type: string;
  /** Target Action. */
  target: string;
  /** Data Action. */
  data: string;
  /** Result Mapping. */
  resultMapping: string;
  /** Header Action. */
  header: string;
}

