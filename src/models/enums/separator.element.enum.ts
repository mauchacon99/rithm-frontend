/**
 * Represents all separator elements of select options.
 */
export enum SeparatorElement {

  /** 'Slash' separator type. */
  slash = '/',

  /** 'Hyphen' separator type. */
  hyphen = '-',

  /** 'Plus' separator type. */
  plusSign = '+',

  /** Ampersand separator type. */
  ampersand = '&'
}
